	<h4 class="topic-heading text-primary" id="about">About</h4>
  <p style="margin-left: 18px;font-size: 1.2em;color: #1a1a1a;"> Have you ever wondered how does a radio work? How does it receive signals from satellites and
     convert them into the languages we can comprehend? If you want to explore this fascinating field, then Ham Radio Club is the place you 
     should be. The main aim of HRC is to establish and operate a ground station in IITB and to increase the Ham or amateur radio culture in
     the institute. A ground station is a very big asset to an institute and even the country as a whole. The ground station can be used to 
     transmit and receive signals from any satellite persistently without human support. A satellite that canâ€™t be tracked from one GS may 
     be tracked by a GS located at a different location, thus, having a proficient ground station network in a country is very beneficial for 
     the space community of the nation. The HRC also conducts a Ground Station Workshop once a year for people from other colleges who want to 
     set up a GS in their institute. Besides this, the Club conducts a lot of interesting talks and fun activities for those who are 
     inquisitive about the functioning of a radio.<br>
    HRC wants to promote the Ham culture in the institute and motivate people to take up amateur radio as a hobby. You can talk to anyone 
    around the world or even in space using a handheld radio or baofeng and can even receive codes for pictures and decipher them later using 
    a software to convert them into normal images. Ham radio has proven to be the last hope of rescue authorities during the times of 
    disasters when all networks were disrupted . You can join us at HRC to know more about the workings of a Ham radio and  various wireless 
    connections. We conduct satellite tracking sessions, antennae making sessions, how things work sessions, and many more similar activities 
    for the radio enthu junta of IITB. Stay tuned to this page for updates on our upcoming events. 
</p>
