            <p>The IIT Bombay Student Satellite Program (IITBSSP)  is a landmark project taken up by IIT Bombay students. The objective of this project is to make IIT Bombay a respected Centre of Excellence in Satellite and Space Technology in the world. These Satellites could be test-beds for new technology that is being developed in the institute and also a method for space qualification.</p>
