              <h2 class="text-primary">Pratham</h2>
            </div>
            
            <p>The first satellite under this program was launched onboard the PSLV C-35 on 26th September 2016. Christened <a href="pratham.php" style="color: blue"><b>Pratham</b></a>, being IIT Bombay's first foray into space technology, the satellite's payload was to produce data on the total electron count in the atmosphere, used for applications like GPS correction. Its beacon signal was received on 28th September and then again in December, at our ground station, providing 90% success to the mission. Downlink signal was never received because of which no payload data was generated.</p>
            <p>Planned, designed, built and tested exclusively by the students, with guidance from Indian Space Research Organization (ISRO) and IIT Bombay faculty, the completely autonomous satellite Pratham took IIT Bombay into the elite space club and served as the first step towards achieving our overall objectives. That’s the reason Pratham is referred to as ‘The First Step Towards Infinity’.</p>
