              <h2 class="text-primary">Advitiy</h2>
            </div>
            <p>After launching the first satellite, the team invested time in developing the concept for its second satellite, <a href="advitiy.php" style="color: blue"><b>Advitiy</b></a> which means 'Second to None'. Advitiy was being designed to be the lightest actively controlled 1U CubeSat. The payload for this mission was to transmit stored and uplinked images via SSTV. In addition to this, the team aimed to create low-cost receivers which could be used to receive these images. During the mission design, the team worked on designing the antenna deployment system. A closed-loop simulation framework was developed to aid the design of Attitude Determination and Controls Subsystem.
            <!-- <br><br> -->
            In parallel to this, the team worked on<a href="wiki.php" style="color: blue"><b> Satellite 101 wiki</b></a>, a systematic and organized compilation of the knowledge and the experience gained by the team through the journey of Pratham, IIT Bombay's first student satellite.</p>
          </div>

          <div class="col-md-12 mt-3" data-aos="fade-up">
            <div class="text-left pb-1 border-primary mb-4">
              <h2 class="text-primary">Other Initiatives</h2>
            </div>
            <p>After the announcement of opportunity for<a href="https://www.isro.gov.in/update/15-jun-2019/announcement-of-opportunity-ao-orbital-platform" style="color: blue"><b> ISRO's PSLV Stage 4 Orbital Platform</b></a>, the team decided to pause work on Advitiy and carry forward development of Antenna Deployment System along with other suitable CubeSat subsystems whose indigenous development could be of relevance. With this objective Antenna Deployment System and Star Tracker based Attitude Determination System are being developed. They are aimed to be tested on the Orbital Platform of stage 4 of PSLV.
            <br><br>
            The team is also working on a lunar mission called<a href="https://www.glee2023.org/" style="color: blue"><b> The Great Lunar Expedition for Everyone (GLEE)</b></a> which is an initiative by the University of Colorado Boulder to deploy 500 five-gram LunaSats, one of which will be designed by us, to the lunar surface lol to conduct local and distributed science missions. Inspired from NASA’s Apollo Moon landings 50 years ago, GLEE will be a catalyst for a new generation of space missions and explorers.
            <br><br>
            The<a href="https://m.facebook.com/HamclubIITB/" style="color: blue"><b> Ham Radio Club, IIT Bombay</b></a> is also part of IITBSSP, which aims to maintain a functional ground station and encourage amateur radio activities in the institute. It organises an annual Groundstation Workshop for engineering colleges around the country to encourage them to set up their own ground station.
            </p>
