                <p class="lead" style="text-align:justify;">'Pratham' is the first satellite under IIT Bombay Student Satellite Project. The plan was to build a fully functional microsatellite which would then be launched by the Indian Space Research Organisation (ISRO). This is entirely a student initiative with mentorship provided by ISRO scientists and IIT Bombay Faculty. The satellite will fit in a 30.5cm X 33.5cm X 46.6cm cube and will weigh 10.15kg. excluding FE Ring. </p>

                <p class="lead" style="text-align:justify;">For the relevance of our satellite to the student community, we will be transmitting satellite data when the satellite passes over India so that any interested university with a small ground station will not only be able to detect the beacon signal from our satellite but will also be able to measure TEC above their ground station. This is proposed to spread awareness among the student community about this exciting field.
