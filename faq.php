        <p align="justify" style="font-weight: bold">Q1. Why is Pratham taking so much time to finish? </p>
        <p align="justify">Ans: One of the things we have stressed in the making of Pratham is making it by ourselves. Hence we have not bought any of the Sub-Systems of the shelf, unlike other student satellites. This is not because we dont have the money, but we want to learn and hence dont want to do it. Secondly, we donot have pre-exisiting facility for satellite making. Hence we are anticipating our needs at each design stage and are setting up our lab simultaneously with building the satellite. It is but natural therefore that we will take more time. </p>

        <p align="justify" style="font-weight: bold">Q2. What is the budget of Pratham? </p>
