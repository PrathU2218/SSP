        <p align="justify">Ans: We have stressed in spreading our knowledge among the student community and giving students free access to our documents. But unfortunately it has backfired and some groups donot give us credit after having used our documents. Hence we will still give free access to the documents, only after a hardcopy of the copyright rule is sent to us by the student team.</p>

        <p align="justify" style="font-weight: bold">Q5. Why does Pratham not make all its Design documents opensource so that the student community can benefit from them?
        <p align="justify">Ans: The Pratham team believes that giving budding satellite teams access to PDR or CDR documents&nbsp;kills their desire to learn and takes away the learning from the design process itself. We have already seen it happen a couple of time and dont want this to happen again. We are more than willing to help student satellite teams and believe that even we can learn from their efforts.&nbsp;Hence, our request to all the student satellite teams is that they first prepare their own conceptual design documents, understand the intricacies of the satellite as a system and then approach us for the documents.&nbsp; </p>

